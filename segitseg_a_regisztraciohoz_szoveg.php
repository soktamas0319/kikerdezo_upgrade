A regisztráció célja, hogy kollégáinknak lehetőséget teremtsünk arra, hogy teszteket lehessen létrehozni
 a Tesztépítő funkció segítségével.<br> 
Ha regisztrálunk az oldalon, akkor lehetőségünk nyílik arra, hogy kérdéssorokat hozzunk létre.<br> 
A regisztráció nem automatikus. Azt, hogy ki lehet regisztrált tag, azt a beérkezett kérelmek alapján az oldal adminisztrátora,
 vagy adminisztrátorai döntik el.<br>
