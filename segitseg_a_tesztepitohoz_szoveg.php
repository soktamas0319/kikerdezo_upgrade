    <td>
      <b>
        <font face="Verdana"><b><u>1. Új téma létrehozása:</b></u></font><br><br>
        Ha kattintunk az Új téma létrehozása gombon, akkor a következő lapon be kell írnunk az új téma nevét.<br>
        Ha a témát hozzáadtuk az adatbázishoz, a kérdés szerkesztőbe jutunk.<br>
        Itt be kell írnunk a kérdést, a kérdésre adható lehetséges válaszokat (legalább kettőt), majd be kell jelölnünk a helyes választ. <br>
        A további kérdések beírásához jelöljük be a tovább rádió gombot, majd kattintsunk a Mehet!-re.<br>
        A kérdéssor szerkesztése közben a megfelelő rádió gombokat bejelölve közlekedhetünk a kérdések között előre, hátra.<br>
        Ha mégsem akarjuk valamelyik kérdést, akkor törölhetjük is azt.<br>
        Ha elkészültünk a kérdéssor összeállításával a Kész gombot kell bejelölnünk, és a Tovább gombra kattintanunk.<br>
        Ekkor a tesztünk bekerül az adatbázisba, és a megjelenik a többi téma között. <br><br>

        <font face="Verdana"><b><u>2. Meglévő kérdéssor módosítása</b></u></font><br><br>
        Ez a funkció lehetővé teszi, hogy a már elkészült teszteken módosításokat végezhessünk el.<br>
        Lehet javítani, bővíteni, illetve törölni is kérdéseket.<br>
        Természetesen mindenki csak a saját kérdéseit módosíthatja, más felhasználók kérdései meg sem jelennek a legördülő listában. <br><br>

        <font face="Verdana"><b><u>3. Kérdéssor törlése </b></u></font><br><br>

        Itt a kiválasztott témát törölheti, a felhasználó, ha úgy dönt, hogy a kérdéssor feleslegessé vált. <br><br>

        <font face="Verdana"><b><u>4. Kérdéssor elrejtése</b></u></font><br><br>

        Erre a funkcióra azért van szükség, hogy ha a tanár számonkérést, vagy gyakorlást szeretne végeztetni a diákokkal,
        akkor csak akkor jelenjen meg a témák között a kérdéssor,
        amikor az aktuálissá válik.<br>
        Ezzel megelőzhetjük azt, hogy a diákok előre fel tudjanak készülni a lehetséges kérdésekből. <br>
        Csak akkor láthatják meg a kérdéssort, mikor a tanár azt jóváhagyja.<br>
        Ha rákattintunk a témák elrejtése/felfedése gombra, a betöltődő lapon láthatja a felhasználó a saját témáit ábécé sorrendben.<br>
        Itt be lehet jelölni, hogy látszódjon-e a téma, vagy sem. A Beállítás kész! gombra kattintva tudjuk rögzíteni a beállításokat.<br><br>




      </b>
